export class Review {
    constructor() {}

    setName(text) {
        this.name = text
    }

    setEmail(text) {
        this.email = text
    }

    setReview(text) {
        this.review = text
    }

    getName() {
        return this.name
    }

    getEmail() {
        return this.email
    }

    getReview() {
        return this.review
    }
}